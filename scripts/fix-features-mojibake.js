const fs = require('fs');
const path = require('path');

const file = path.join(__dirname, '..', 'apps', 'web', 'src', 'app', 'features', 'page.tsx');
let content = fs.readFileSync(file, 'utf-8');

// Reconstruct mojibake strings from the original UTF-8 bytes interpreted as Latin-1.
// This avoids any ambiguity with non-breaking spaces or variation selectors.
function latin1(bytes) {
  return Buffer.from(bytes).toString('latin1');
}

const replacements = [
  [latin1([0xF0, 0x9F, 0x94, 0x8E]), '🔎'],
  [latin1([0xF0, 0x9F, 0x94, 0x8D]), '🔍'],
  [latin1([0xF0, 0x9F, 0x8F, 0xAA]), '🏪'],
  [latin1([0xF0, 0x9F, 0x8F, 0xAC]), '🏬'],
  [latin1([0xF0, 0x9F, 0x9B, 0x92]), '🛒'],
  [latin1([0xF0, 0x9F, 0x93, 0x88]), '📈'],
  [latin1([0xF0, 0x9F, 0x93, 0x8A]), '📊'],
  [latin1([0xF0, 0x9F, 0x8E, 0xAF]), '🎯'],
  [latin1([0xF0, 0x9F, 0x9A, 0x80]), '🚀'],
  [latin1([0xF0, 0x9F, 0x92, 0xB3]), '💳'],
  [latin1([0xF0, 0x9F, 0x92, 0xB0]), '💰'],
  [latin1([0xF0, 0x9F, 0x94, 0x84]), '🔁'],
  [latin1([0xF0, 0x9F, 0x93, 0xB1]), '📱'],
  [latin1([0xF0, 0x9F, 0x8E, 0x89]), '🎉'],
  [latin1([0xF0, 0x9F, 0xA4, 0x9D]), '🤝'],
  [latin1([0xF0, 0x9F, 0x86, 0x93]), '🆓'],
  [latin1([0xF0, 0x9F, 0x92, 0xA1]), '💡'],
  [latin1([0xE2, 0x80, 0x93]), '–'],
  [latin1([0xE2, 0x80, 0x94]), '—'],
  [latin1([0xE2, 0x86, 0x92]), '→'],
  [latin1([0xE2, 0x86, 0x93]), '↓'],
  [latin1([0xE2, 0x80, 0xA2]), '•'],
  [latin1([0xE2, 0x9D, 0x8C]), '❌'],
  [latin1([0xE2, 0x9C, 0x85]), '✅'],
  [latin1([0xE2, 0x9C, 0x93]), '✓'],
  [latin1([0xE2, 0x98, 0x85]), '★'],
  [latin1([0xE2, 0x9C, 0x97]), '✗'],
  [latin1([0xE2, 0x9A, 0xA1]), '⚡'],
  [latin1([0xE2, 0x9A, 0xA0, 0xEF, 0xB8, 0x8F]), '⚠️'],
  [latin1([0xE2, 0x9A, 0xA0]), '⚠'],
  [latin1([0xE2, 0x8F, 0xB1, 0xEF, 0xB8, 0x8F]), '⏱️'],
  [latin1([0xE2, 0x9C, 0xA8]), '✨'],
];

let total = 0;
for (const [bad, good] of replacements) {
  const count = (content.match(new RegExp(bad.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')) || []).length;
  if (count > 0) {
    content = content.split(bad).join(good);
    total += count;
    console.log(`${count} replacements of ${JSON.stringify(bad)} -> ${JSON.stringify(good)}`);
  }
}

fs.writeFileSync(file, content, 'utf-8');
console.log(`\nTotal replacements: ${total}`);
